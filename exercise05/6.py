# 6．猴子吃桃问题：猴子第一天摘下若干个桃子，当即吃了一半，还不瘾，又多吃了一个　　　第二天早
# 上又将剩下的桃子吃掉一半，又多吃了一个。以后每天早上都吃了前一天剩下的一半零一个。到第10天早
# 上想再吃时，见只剩下一个桃子了。求第一天共摘了多少。程序分析：采取逆向思维的方法，从后往前推
# 断。
Num = 1
count = 0
#逐天求出每天吃之前桃子数量
while 1:
    Num = (Num+1)*2
    count = count + 1
    if count>=10:
        break
print('第一天摘了%d个桃子'%Num)
print(count)